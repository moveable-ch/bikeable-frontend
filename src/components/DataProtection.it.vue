    <h2 id="erfolgs-und-reichweitenmessung">
      11. misurazione del successo e della portata
    </h2>

    <p>
      Cerchiamo di determinare come viene utilizzata la nostra offerta online.
      In questo contesto, possiamo ad esempio misurare il successo e la portata
      delle nostre attività e operazioni, nonché l'effetto dei link di terzi al
      nostro sito web. Ma possiamo anche, ad esempio, provare e confrontare
      l'utilizzo di diverse parti o versioni della nostra offerta online (metodo
      "A/B test"). Sulla base dei risultati della misurazione del successo e
      della portata, possiamo in particolare correggere gli errori, rafforzare i
      contenuti più popolari o apportare miglioramenti alla nostra offerta
      online.
    </p>

    <p>
      Nella maggior parte dei casi, gli indirizzi IP (Internet Protocol) dei
      singoli utenti vengono memorizzati per misurare le prestazioni e la
      portata. In questo caso, gli indirizzi IP vengono
      <em>sempre</em> abbreviati ("mascheratura IP") per rispettare il principio
      di economia dei dati attraverso la corrispondente pseudonimizzazione.
    </p>

    <p>
      I cookie possono essere utilizzati per misurare il successo e la portata e
      possono essere creati profili utente. I profili utente creati includono,
      ad esempio, le singole pagine visitate o i contenuti visualizzati sul
      nostro sito web, informazioni sulle dimensioni dello schermo o della
      finestra del browser e la posizione - almeno approssimativa -.
      <em>In linea di principio,</em> i profili utente vengono creati
      esclusivamente in forma pseudonimizzata e non vengono utilizzati per
      identificare i singoli utenti. I singoli servizi di terzi presso i quali
      gli utenti sono registrati possono assegnare l'utilizzo dei nostri servizi
      online all'account utente o al profilo utente del rispettivo servizio.
