    <p>
      I cookie possono essere memorizzati nel browser temporaneamente come
      "cookie di sessione" o per un certo periodo di tempo come cosiddetti
      cookie permanenti. I "cookie di sessione" vengono cancellati
      automaticamente alla chiusura del browser. I cookie permanenti hanno un
      periodo di conservazione specifico. I cookie consentono in particolare di
      riconoscere un browser alla successiva visita del nostro sito web e
      quindi, ad esempio, di misurare la portata del nostro sito. I cookie
      permanenti possono essere utilizzati anche per il marketing online, ad
      esempio.
    </p>

    <p>
      I cookie possono essere disattivati e cancellati completamente o
      parzialmente nelle impostazioni del browser in qualsiasi momento. Senza i
      cookie, il nostro sito web potrebbe non essere più completamente
      disponibile. Chiediamo attivamente il vostro consenso esplicito all'uso
      dei cookie, almeno se e nella misura necessaria.
