      visitées ou les contenus consultés sur notre site web, des indications sur
      la taille de l'écran ou de la fenêtre du navigateur et la localisation -
      du moins approximative. <em>En principe</em>, les éventuels profils
      d'utilisateurs sont établis exclusivement sous forme de pseudonymes et ne
      sont pas utilisés pour identifier des utilisateurs individuels. Certains
      services de tiers auxquels les utilisateurs sont inscrits peuvent
      éventuellement associer l'utilisation de notre offre en ligne au compte ou
